import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import {
  enabledRouteConfigs,
  enabledSolutionUiConfigs,
  parseEnabledSolutions,
  SOLUTION_UI_CONFIGS,
} from "./solution-route-config.mjs";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(scriptDir, "..");
const generatedRoot = path.join(appRoot, "src", "app", "(tenant)", "(generated-solutions)");
const generatedLibRoot = path.join(appRoot, "src", "lib");
const generatedManifestsPath = path.join(generatedLibRoot, "generated-solution-manifests.ts");
const generatedWidgetsPath = path.join(generatedLibRoot, "generated-solution-dashboard-widgets.tsx");

function manifestsSource(configs) {
  const imports = configs
    .map((config, index) => `import { manifest as manifest${index} } from "${config.manifestModule}";`)
    .join("\n");
  const exports = configs.map((_, index) => `manifest${index}`).join(", ");

  return `// Auto-generated by apps/web/scripts/generate-solution-routes.mjs
import type { SolutionUIManifest } from "@/lib/solution-manifest-types";
${imports ? `${imports}\n` : ""}
export const GENERATED_SOLUTION_MANIFESTS: SolutionUIManifest[] = [${exports}];
`;
}

function widgetsSource(enabledSolutions) {
  const lines = [
    `// Auto-generated by apps/web/scripts/generate-solution-routes.mjs`,
    `"use client";`,
    ``,
    `import dynamic from "next/dynamic";`,
    `import type { ComponentType } from "react";`,
    ``,
    `const EmptyDashboardWidget: ComponentType<any> = () => null;`,
    ``,
  ];

  for (const config of SOLUTION_UI_CONFIGS) {
    if (!config.dashboardWidgetName || !config.dashboardWidgetModule) {
      continue;
    }
    if (enabledSolutions.has(config.solutionName)) {
      lines.push(
        `export const ${config.dashboardWidgetName} = dynamic(() => import("${config.dashboardWidgetModule}"), { ssr: false });`,
      );
    } else {
      lines.push(`export const ${config.dashboardWidgetName}: ComponentType<any> = EmptyDashboardWidget;`);
    }
  }

  lines.push("");
  return lines.join("\n");
}

async function main() {
  const enabledSolutions = parseEnabledSolutions();
  const routeConfigs = enabledRouteConfigs(enabledSolutions);
  const uiConfigs = enabledSolutionUiConfigs(enabledSolutions);

  await fs.rm(generatedRoot, { recursive: true, force: true });
  await fs.mkdir(generatedRoot, { recursive: true });
  await fs.mkdir(generatedLibRoot, { recursive: true });

  for (const config of routeConfigs) {
    const routeDir = path.join(generatedRoot, ...config.routeSegments);
    await fs.mkdir(routeDir, { recursive: true });
    const pagePath = path.join(routeDir, "page.tsx");
    const componentName = config.sourceModule
      .split("/")
      .pop()
      .replace(/-([a-z])/g, (_, c) => c.toUpperCase())
      .replace(/^([a-z])/, (_, c) => c.toUpperCase());
    const wrapperName = `${componentName}Wrapper`;
    const source = [
      `// Auto-generated by apps/web/scripts/generate-solution-routes.mjs`,
      `"use client";`,
      ``,
      `import { useTenantSolutionState } from "@/lib/solutions";`,
      `import ${componentName} from "${config.sourceModule}";`,
      ``,
      `export default function ${wrapperName}() {`,
      `  const solutionState = useTenantSolutionState("${config.solutionName}");`,
      `  return <${componentName} solutionState={solutionState} />;`,
      `}`,
      ``,
    ].join("\n");
    await fs.writeFile(pagePath, source, "utf-8");
  }

  await fs.writeFile(generatedManifestsPath, manifestsSource(uiConfigs), "utf-8");
  await fs.writeFile(generatedWidgetsPath, widgetsSource(enabledSolutions), "utf-8");
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
